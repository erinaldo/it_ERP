            }
        }

        private void Llamar_formulario(Cl_Enumeradores.eTipo_action Accion)
        {
            try
            {
                frm = new frmGe_banco_procesos_bancarios_tipo_mant();
                frm.event_frmGe_banco_procesos_bancarios_tipo_mant_FormClosing += frm_event_frmGe_banco_procesos_bancarios_tipo_mant_FormClosing;
                frm.Set_Accion(Accion);
                if (Accion != Cl_Enumeradores.eTipo_action.grabar)
                {
                    if (Info_Proceso != null)
                    {
                        frm.Set_Info(Info_Proceso);
                        frm.MdiParent = this.MdiParent;
                        frm.Show();
                    }
                    else
                        MessageBox.Show("Por Favor seleccione un registro para continuar", "Sistemas", MessageBoxButtons.OK, MessageBoxIcon.Information);
                }
                else
                {
                    frm.MdiParent = this.MdiParent;
                    frm.Show();
                }
            }
            catch (Exception ex)
            {
                string NameMetodo = System.Reflection.MethodBase.GetCurrentMethod().Name;
                MessageBox.Show(param.Get_Mensaje_sys(enum_Mensajes_sys.Error_comunicarse_con_sistemas) + ex.Message + " ", param.Nombre_sistema, MessageBoxButtons.OK, MessageBoxIcon.Error);
                Log_Error_bus.Log_Error(NameMetodo + " - " + ex.ToString());
            }
        }

