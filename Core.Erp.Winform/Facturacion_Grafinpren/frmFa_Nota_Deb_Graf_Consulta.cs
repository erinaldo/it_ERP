        private void llama_frm(Cl_Enumeradores.eTipo_action Accion)
        {
            try
            {

                frm = new frmFa_Nota_Deb_Graf_Mant();
                frm.Event_frmFA_NotaCreditoDebito_FormClosing += frm_Event_frmFA_NotaCreditoDebito_FormClosing;
                frm.MdiParent = this.MdiParent;
                if (Accion != Cl_Enumeradores.eTipo_action.grabar)
                {
                    if (Info != null)
                    {
                        frm.SetInfo(Info);
                        frm.setAccion(Accion);
                        frm.Show();
                    }
                    else
                        MessageBox.Show("Para continuar debe seleccionar un registro", "Sistemas", MessageBoxButtons.OK, MessageBoxIcon.Information);
                }
                else
                {
                    frm.setAccion(Accion);
